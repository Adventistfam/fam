// Classe para gerenciar traduções
class TranslationManager {
    constructor() {
        this.currentLang = this.detectLanguage();
        this.init();
    }

    // Detecta o idioma do navegador ou usa o salvo
    detectLanguage() {
        const savedLang = localStorage.getItem('adventist-fam-lang');
        if (savedLang && translations[savedLang]) {
            return savedLang;
        }
        
        const browserLang = navigator.language || navigator.userLanguage;
        const langCode = browserLang.split('-')[0].toLowerCase();
        
        return translations[langCode] ? langCode : 'pt';
    }

    // Inicializa o sistema de tradução
    init() {
        this.updatePageLanguage();
        this.createLanguageToggle();
    }

    // Alterna entre idiomas
    toggleLanguage() {
        this.currentLang = this.currentLang === 'pt' ? 'en' : 'pt';
        localStorage.setItem('adventist-fam-lang', this.currentLang);
        this.updatePageLanguage();
        this.updateLanguageToggle();
    }

    // Define o idioma manualmente
    setLanguage(lang) {
        if (translations[lang]) {
            this.currentLang = lang;
            localStorage.setItem('adventist-fam-lang', lang);
            this.updatePageLanguage();
            this.updateLanguageToggle();
        }
    }

    // Atualiza todos os elementos da página
    updatePageLanguage() {
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            if (translations[this.currentLang][key]) {
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = translations[this.currentLang][key];
                } else {
                    element.textContent = translations[this.currentLang][key];
                }
            }
        });

        // Atualiza o atributo lang do HTML
        document.documentElement.lang = this.currentLang === 'pt' ? 'pt-BR' : 'en';
    }

    // Cria o botão de alternância de idioma
    createLanguageToggle() {
        const existingToggle = document.getElementById('language-toggle');
        if (existingToggle) return;

        const toggle = document.createElement('div');
        toggle.id = 'language-toggle';
        toggle.className = 'language-toggle';
        toggle.innerHTML = `
            <button class="lang-btn ${this.currentLang === 'pt' ? 'active' : ''}" data-lang="pt">PT</button>
            <span>|</span>
            <button class="lang-btn ${this.currentLang === 'en' ? 'active' : ''}" data-lang="en">EN</button>
        `;

        toggle.querySelectorAll('.lang-btn').forEach(btn => {
            btn.addEventListener('click', (e) => {
                const lang = e.target.getAttribute('data-lang');
                this.setLanguage(lang);
            });
        });

        document.body.appendChild(toggle);
    }

    // Atualiza o estado visual do botão de idioma
    updateLanguageToggle() {
        const toggle = document.getElementById('language-toggle');
        if (toggle) {
            toggle.querySelectorAll('.lang-btn').forEach(btn => {
                btn.classList.toggle('active', btn.getAttribute('data-lang') === this.currentLang);
            });
        }
    }

    // Obtém uma tradução específica
    get(key) {
        return translations[this.currentLang][key] || translations['pt'][key] || key;
    }
}

// Inicializa o gerenciador de traduções quando o DOM estiver pronto
let translationManager;
document.addEventListener('DOMContentLoaded', () => {
    translationManager = new TranslationManager();
});

// Função global para alternar idioma
function toggleLanguage() {
    if (translationManager) {
        translationManager.toggleLanguage();
    }
}

// Função global para obter tradução
function t(key) {
    return translationManager ? translationManager.get(key) : key;
}
